#include <math.h>
#include <stdio.h>

int main() {
    double x1 = 0; // Сумма элементов главной диагонали
    double x2 = 0; // Сумма элементов побочной диагонали
    
    int n;
    
    printf("Enter the number of matrices: "); // Запрос ввода числа матриц
    scanf("%d", &n); // Считывание числа матриц
    
    double arr[n][n]; // Объявление двумерного массива (матрицы) размером n x n
    
    // Ввод элементов первой матрицы
    for (int i = 0; i<n; i++){
        for (int j = 0; j<n; j++){
            printf("Enter the matrix A [%d][%d]:", i, j); // Запрос элемента матрицы
            scanf("%lf", &arr[i][j]); // Считывание элемента матрицы с клавиатуры
        }
    }
    printf("\n");
    
    // Вывод элементов первой матрицы
    for (int i = 0; i<n; i++){
        for (int j = 0; j<n; j++){
            printf("%3lf ", arr[i][j]); // Вывод элемента матрицы с форматированием
        }
        printf("\n");
    }
    printf("\n");
    
    // Вычисление суммы элементов главной и побочной диагоналей
    for (int i = 0; i<n; i++){
        x1+=arr[i][i]; // Суммирование элементов главной диагонали
        x2+=arr[i][n-i-1]; // Суммирование элементов побочной диагонали
    }
    printf("X1 = %lf\nX2 = %lf\n", x1, x2); // Вывод сумм диагоналей
    
    int m=1, arr2[2][2], arr3[2][2]; // Объявление переменных: m - для вычислений, arr2 - матрица 2x2, arr3 - результат
    
    // Ввод элементов второй матрицы (2x2)
    for (int i = 0; i<2; i++){
        for (int j = 0; j<2; j++){
            printf("Enter the matrix B [%d][%d]:", i, j); // Запрос элемента матрицы
            scanf("%d", &arr2[i][j]); // Считывание элемента матрицы с клавиатуры
        }
    }
    
    // Вычисление квадрата второй матрицы
    for (int i = 0; i<2; i++){
        for (int j = 0; j<2; j++){
            m=0; // Переменная m обнуляется перед каждым вычислением элемента матрицы arr3
            for (int k = 0; k<2; k++){
                m+=arr2[i][k]*arr2[k][j]; // Вычисление элемента матрицы arr3[i][j]
            }
            arr3[i][j]=m; // Присваивание вычисленного значения элементу матрицы arr3[i][j]
        }
    }
    
    // Вывод квадрата второй матрицы
    for (int i = 0; i<2; i++){
        for (int j = 0; j<2; j++){
            printf("%2d ", arr3[i][j]); // Вывод элемента матрицы arr3 с форматированием
        }
        printf("\n"); // Переход на новую строку после каждой строки матрицы
    }
    
    return 0;
}
