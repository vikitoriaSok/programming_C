//ЗАДАЧА С ОБЪЯСНЕНИЕМ
#include <stdio.h>
#include <math.h>

int main() {

  double a[3][3], sum1 = 0, sum2 = 0; // Объявление двумерного массива double размером 3x3 (a) и инициализация двух переменных типа double для хранения сумм диагоналей (sum1, sum2).
  int i, j, n, N, k; // Объявление целочисленных переменных для использования в циклах (i, j, k), для хранения размера второй матрицы (n), общего количества элементов во второй матрице (N).

  printf("Enter 9 numbers for the array double:\n"); // Вывод приглашения для ввода 9 чисел с плавающей точкой для первого массива.
  for (i = 0; i < 3; i++)                     // Внешний цикл по строкам массива a.
    for (j = 0; j < 3; j++)                    // Внутренний цикл по столбцам массива a.
      scanf("%lf", &a[i][j]);                 // Считывание числа с плавающей точкой и сохранение его в элементе a[i][j].

  for (i = 0; i < 3; i++) {               // Цикл для вычисления сумм диагоналей массива a.
    sum1 += a[i][i];                           // Прибавление элемента главной диагонали (a[i][i]) к sum1.
    sum2 += a[i][2 - i];                          // Прибавление элемента побочной диагонали (a[i][2-i]) к sum2.
  }

  printf("The sum of the main diagonal: %.2lf\n", sum1);   // Вывод суммы главной диагонали (sum1).
  printf("The sum of the side diagonal: %.2lf\n", sum2);      // Вывод суммы побочной диагонали (sum2).
  printf("Enter n (n * n): ");  // Вывод приглашения для ввода размера второй матрицы (n).
  scanf("%d", &n); // Считывание размера второй матрицы (n).

  int b[n][n], c[n][n]; // Объявление двух двумерных целочисленных массивов размером n x n: b - для хранения исходной матрицы, c - для хранения результата умножения.
  N = pow(n, 2); // Вычисление общего количества элементов в массиве b.  

  printf("\nEnter %d chisel\n", N); // Вывод приглашения для ввода N чисел (где N = n*n) для второго массива.
  for (i = 0; i < n; i++) {  // Внешний цикл по строкам массива b.
    for (j = 0; j < n; j++) {  // Внутренний цикл по столбцам массива b.
      scanf("%d", &b[i][j]); // Считывание целого числа и сохранение его в элементе b[i][j].
    }
  }

  
  for (i = 0; i < n; i++) { // Внешний цикл по строкам результирующей матрицы c.
    for (j = 0; j < n; j++) { // Внутренний цикл по столбцам результирующей матрицы c.
      c[i][j] = 0; // Инициализация элемента c[i][j] нулем.  Это важно, чтобы правильно вычислить сумму произведений.
      for (k = 0; k < n; k++) { // Цикл для вычисления элемента c[i][j] как суммы произведений элементов i-й строки матрицы b и j-го столбца матрицы b.
        c[i][j] += b[i][k] * b[k][j]; // c[i][j] = c[i][j] + b[i][k] * b[k][j];  Вычисление суммы произведений элементов i-й строки b и j-го столбца b.
      }
    }
  }

  printf("\n:\n"); // Вывод разделителя перед выводом результирующей матрицы.
  for (i = 0; i < n; i++) { // Внешний цикл по строкам матрицы c.
    for (j = 0; j < n; j++) { // Внутренний цикл по столбцам матрицы c.
      printf("%d ", c[i][j]); // Вывод элемента c[i][j] с пробелом.
    }
    printf("\n"); // Переход на новую строку после каждой строки матрицы.
  }

  return 0; // Завершение программы.
}
