#include <stdio.h>
#include <stdlib.h>

/* 1. Объявить массив данных типа double размером 3 на 3. 
2. Ввести с консоли его значения, 
3. вывести на консоль сумму его элементов, расположенных на главной диагонали 
4. и сумму элементов, расположенных на  побочной диагонали.
5. Объявить массив данных типа int размером 2 на 2. 
6. Ввести с консоли его значе-ния, 
7. вывести на консоль квадрат данной матрицы.*/


int main() { 

	double matrix_double[3][3]; // Объявляем массив double размером 3 на 3
                               // double означает, что массив будет хранить числа с плавающей точкой (например, 3.14)

  double sum_main_diag = 0.0;   // Объявляем переменную для суммы главной диагонали (изначально 0)
  double sum_secondary_diag = 0.0; // Объявляем переменную для суммы побочной диагонали (изначально 0)

  int i, j; // Объявляем переменные i и j для использования в циклах

  printf("enter the array elements double 3x3:\n"); // Выводим сообщение для пользователя
  for (i = 0; i < 3; i++) {        // Внешний цикл: проходим по строкам массива (от 0 до 2)
    for (j = 0; j < 3; j++) {      // Внутренний цикл: проходим по столбцам каждой строки (от 0 до 2)
      printf("matrix_double[%d][%d]: ", i, j); // Выводим подсказку, какой элемент нужно ввести
      scanf("%lf", &matrix_double[i][j]);   // Считываем число типа double и сохраняем его в массив
                                           // %lf - спецификатор формата для чтения double
                                           // &matrix_double[i][j] - адрес ячейки памяти, куда нужно сохранить число
    }
  }

  // Вычисление суммы главной диагонали
  for (i = 0; i < 3; i++) {      // Цикл: проходим по элементам главной диагонали
    sum_main_diag = sum_main_diag + matrix_double[i][i]; // Прибавляем элемент к сумме
                                                         // На главной диагонали индексы строки и столбца совпадают (0,0; 1,1; 2,2)
  }

  // Вычисление суммы побочной диагонали
  for (i = 0; i < 3; i++) {      // Цикл: проходим по элементам побочной диагонали
    sum_secondary_diag = sum_secondary_diag + matrix_double[i][2 - i]; // Прибавляем элемент к сумме
                                                                       // На побочной диагонали сумма индексов строки и столбца равна 2 (0,2; 1,1; 2,0)
  }

  printf("The sum of the main diagonal: %.2lf\n", sum_main_diag);   // Выводим сумму главной диагонали
                                                              // %.2lf - спецификатор формата для вывода double с 2 знаками после запятой
	  printf("The sum of the side diagonal: %.2lf\n", sum_secondary_diag); // Выводим сумму побочной диагонали

  // ********************************************************
  // Часть 2: Массив int 2x2
  // ********************************************************

  int matrix_int[2][2];       // Объявляем массив int размером 2 на 2
                               // int означает, что массив будет хранить целые числа (например, 1, 2, 3)
  int result_matrix[2][2];    // Объявляем массив для хранения квадрата матрицы

  printf("\nEnter the array elements int 2x2:\n");  // Выводим сообщение для пользователя
  for (i = 0; i < 2; i++) {        // Внешний цикл: проходим по строкам массива (от 0 до 1)
    for (j = 0; j < 2; j++) {      // Внутренний цикл: проходим по столбцам каждой строки (от 0 до 1)
      printf("matrix_int[%d][%d]: ", i, j); // Выводим подсказку, какой элемент нужно ввести
      scanf("%d", &matrix_int[i][j]);      // Считываем целое число и сохраняем его в массив
                                          // %d - спецификатор формата для чтения int
    }
  }

  // Вычисление квадрата матрицы (простое умножение матрицы на себя)
  // result_matrix[0][0] = (matrix_int[0][0] * matrix_int[0][0]) + (matrix_int[0][1] * matrix_int[1][0]);
  result_matrix[0][0] = matrix_int[0][0] * matrix_int[0][0] + matrix_int[0][1] * matrix_int[1][0];  // Corrected:  Fixed parentheses and removed incorrect indexing.
  result_matrix[0][1] = matrix_int[0][0] * matrix_int[0][1] + matrix_int[0][1] * matrix_int[1][1];  // Corrected:  Removed incorrect indexing.

  result_matrix[1][0] = matrix_int[1][0] * matrix_int[0][0] + matrix_int[1][1] * matrix_int[1][0];  // Corrected:  Removed incorrect indexing.
  result_matrix[1][1] = matrix_int[1][0] * matrix_int[0][1] + matrix_int[1][1] * matrix_int[1][1];  // Corrected:  Removed incorrect indexing.
  // Вывод результата
  printf("\nThe square of the matrix:\n");
  for (i = 0; i < 2; i++) {        // Внешний цикл: проходим по строкам результирующего массива
    for (j = 0; j < 2; j++) {      // Внутренний цикл: проходим по столбцам каждой строки
      printf("%d ", result_matrix[i][j]); // Выводим элемент массива
    }
    printf("\n");                    // Переходим на новую строку после вывода каждой строки массива
  }

  return 0; // Указываем, что программа завершилась успешно
}

